def count_pos(text):
    blob = TextBlob(str(text))

    adj_count = 0
    noun_count = 0
    verb_count = 0

    for word, tag in blob.tags:
        if tag.startswith("JJ"):
            adj_count += 1
        elif tag.startswith("NN"):
            noun_count += 1
        elif tag.startswith("VB"):
            verb_count += 1

    return pd.Series([adj_count, noun_count, verb_count])


