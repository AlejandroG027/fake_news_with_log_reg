# Function to reduce cats in speaker_job
def group_speaker_job(job):
    if isna(job):
        return "Non-Government"
    job = job.lower()
    if any(x in job for x in ['senator', 'representative', 'congress', 'legislator', 'governor', 'president', 'mayor', 'executive']):
        return "Government"
    else:
        return "Non-Government"
# Function to reduce cats in context var
def group_context(context):
    if isna(context):
        return "Other"
    context = context.lower()
    if any(x in context for x in ['interview', 'interviewee', 'interviewing',]):
        return "Interview"
    elif any(x in context for x in ["speech", "debate","debated", "town hall", "press conference","campaign rally",
                                    "address",'press','forum']):
        return "Speech"
    elif any(x in context for x in ["ad", "advertisement", "commercial", "video",
                                    "film", "media", "article", "book", "novel","podcast", "radio", "tv",
                                    "television","abc","cnn","fox",'show','flier','email']):
        return "Media"
    elif any(x in context for x in ["tweet", "twitter", "facebook", "instagram","social media", "post",
                                    'web', 'internet','blog','op-ed']):
        return "Social_Media"
    else:
        return "Other"

